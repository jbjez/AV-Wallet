    StateNotifierProvider<TranslationService, Locale>((ref) => TranslationService());

// Extension pour faciliter l'utilisation
extension TranslationExtension on BuildContext {
  String t(String key) {
    // On doit utiliser le provider pour obtenir l'instance correcte
    final container = ProviderScope.containerOf(this);
    final translationService = container.read(translationServiceProvider.notifier);
    return translationService.t(key);
  }
}
